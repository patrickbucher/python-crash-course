print('Are those French Fries? I think so!')
print(dish == 'French Fries')

print('Is that the same as "french fries"? I do not think so!')
print(dish == 'french fries')

print('Is it case insensitively the same? Oh, yes!')
print(dish.lower() == 'french fries')

